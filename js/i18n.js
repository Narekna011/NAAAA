    function applyTranslations(lang){
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.dataset.i18n;
            const val = (TRANSLATIONS[lang] && TRANSLATIONS[lang][key]) || (TRANSLATIONS['en'] && TRANSLATIONS['en'][key]) || el.textContent;
            el.textContent = val;
        });
    }

    const saved = localStorage.getItem('lux_lang') || 'hy';
    applyTranslations(saved);

    window.addEventListener('lux:langchange', (e) => {
        const l = e && e.detail && e.detail.lang ? e.detail.lang : (localStorage.getItem('lux_lang') || 'hy');
        applyTranslations(l);
    });

    // expose utility
    window.luxTranslate = applyTranslations;
